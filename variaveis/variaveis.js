//conceitos de variáveis em JavaScript
//variaveis separa um espaço de memoria para armazenar dados

    /*
        -var -> Descontinuado / nao usar
        -let -> valor pode ser declado novamente
        -const -> valor NAO pode ser decladao novamente
    */

var nome = "João"; // variável global ou de função
let idade = 25; // variável de bloco
const pi = 3.14; // constante de bloco



//-------------------------------------------------------------------------//
console.log("Nome:", nome);// exibindo valores no console
console.log("Idade:", idade);//
console.log("Valor de pi:", pi);// exibindo valores no console
console.log("Deu certo!");// exibindo uma mensagem no console
idade = 26; // alterando o valor da variável 'idade'
console.log("Idade atualizada:", idade);
 
